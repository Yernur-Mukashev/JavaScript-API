// Массив объектов товаров
const products = [
	{ name: "Смартфон", category: "Электроника" },
	{ name: "Ноутбук", category: "Электроника" },
	{ name: "Футболка", category: "Одежда" },
	{ name: "Джинсы", category: "Одежда" },
	{ name: "Стул", category: "Дом" },
	{ name: "Стол", category: "Дом" },
];

// Функция для отображения товаров на основе выбранной категории
function displayProducts(category) {
	const productList = document.getElementById("productList");
	productList.innerHTML = ""; // Очистить текущий список товаров

	const filteredProducts = products.filter(
		(product) => category === "" || product.category === category
	);

	filteredProducts.forEach((product) => {
		const productItem = document.createElement("div");
		productItem.className = "product-item";
		productItem.innerHTML = `
            <h3>${product.name}</h3>
            <p>Категория: ${product.category}</p>
        `;
		productList.appendChild(productItem);
	});
}

// Обработчик событий для изменения категории в выпадающем списке
document
	.getElementById("categorySelect")
	.addEventListener("change", function () {
		displayProducts(this.value);
	});

// Изначально отображаем все товары
displayProducts("");
